package batailleespagnole;

/**
 * Interface comprenant toutes les configurations du jeu.
 */
public interface IConfig {

    /**
     * La largeur de la carte en pixels.
     */
    int LARGEUR = 800;
    /**
     * La hauteur de la carte en pixels.
     */
    int HAUTEUR = 600;
    /**
     * La position x de la fenêtre sur l'écran.
     */
    int POSITION_X = 100;
    /**
     * La position y de la fenêtre sur l'écran.
     */
    int POSITION_Y = 50;
}
